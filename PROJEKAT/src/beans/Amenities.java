package beans;

public class Amenities {
	
	private String content;
	
	public Amenities(String content) {
		super();
		this.content = content;
	}

	public Amenities() {
		super();
		content = "";
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}
	
	
	
	
}
